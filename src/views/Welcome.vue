<template>
  <div class="welcome container">
      <p>Welcome</p>
      <div v-if="showLogin">
        <h1>Login</h1>
        <LoginForm @login="enterChat" />
        <p>No account yet? <span class="alt-form" @click="showLogin = false">Sign Up</span></p>
      </div>
      <div v-else>
        <h1>Sign Up</h1>
        <SignupForm @signup="enterChat" />
        <p>Have an account? <span class="alt-form" @click="showLogin = true">Login</span></p>
      </div>
  </div>
</template>

<script>
import SignupForm from '../components/SignupForm.vue';
import LoginForm from '../components/LoginForm.vue';
import { ref } from 'vue';
import { useRouter } from 'vue-router';

export default {
  components: {SignupForm, LoginForm},
  setup(){
    const showLogin = ref(true); //toggles between true/false - true if login page false if signup page
    const router = useRouter();

    const enterChat = () => {
      router.push({ name: 'Chatroom'}); //redirect to chatroom page
    }

    return { showLogin, enterChat }
  }
}
</script>

